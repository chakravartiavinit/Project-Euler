bool check_palindrome(string &a)
{
    int n = a.length();
    for (int i = 0; i < n / 2; ++i)
    {
        if (a[i] != a[n - i - 1])
        {
            return false;
        }
    }
    return true;
}
string ADD(string &a, string &b)
{
    string zero;
    int n = a.length();
    int m = b.length();
    for (int i = 0; i < abs(n - m); ++i)
    {
        zero.push_back('0');
    }
    if (n > m)
    {
        b = zero + b;
    }
    else
    {
        a = zero + a;
    }
    string answer;
    int i = a.length() - 1, sum = 0, carry = 0;
    while (i >= 0)
    {
        sum = (a[i] - '0') + (b[i] - '0') + carry;
        carry = sum / 10;
        sum = sum % 10;
        answer.push_back(sum + '0');
        i--;
    }
    if (carry == 1)
    {
        answer.push_back('1');
    }
    reverse(answer.begin(), answer.end());
    return answer;
}
