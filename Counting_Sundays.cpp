vector<int> normal_months = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
vector<int> leap_months = {31, 29, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
bool check_leap(int year)
{
    if (year % 400 == 0 || year % 100 == 0 || year % 4 == 0)
    {
        return true;
    }
    return false;
}
int Sundays(int first_day, int year)
{
    int answer = 0;
    int day = first_day;
    if (check_leap(year) == true)
    {
        for (int i = 0; i < leap_months.size(); ++i)
        {
            int days_of_month = leap_months[i];
            int to_add = days_of_month % 7;
            day = (day + to_add) % 7;
            if (day == 0)
            {
                answer++;
            }
        }
    }
    else
    {
        for (int i = 0; i < normal_months.size(); ++i)
        {
            int days_of_month = leap_months[i];
            int to_add = days_of_month % 7;
            day = (day + to_add) % 7;
            if (day == 0)
            {
                answer++;
            }
        }
    }
    return answer;
}
